namespace Plugin.Sample.Bootcamp.Exercises
{
    using System;
    using System.Reflection;
    using Microsoft.Extensions.DependencyInjection;
    using Sitecore.Commerce.Core;
    using Sitecore.Framework.Configuration;
    using Sitecore.Framework.Rules;
    using Sitecore.Framework.Rules.Registry;

    /// <summary>
    /// The configure sitecore class.
    /// </summary>
    public class ConfigureSitecore : IConfigureSitecore
    {
        /// <summary>
        /// The configure services.
        /// </summary>
        /// <param name="services">
        /// The services.
        /// </param>
        public void ConfigureServices(IServiceCollection services)
        {
            var assembly = Assembly.GetExecutingAssembly();
            services.RegisterAllPipelineBlocks(assembly);

            services.Sitecore().Rules((Action<IRulesConfigBuilder>)(config =>
                config.Registry((Action<IReflectionDiscoveryConfig>)(registry => registry.RegisterAssembly(assembly)))));
        }
    }
}